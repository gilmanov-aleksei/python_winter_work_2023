# str2 = input("Введите слова через пробел: ")
s1 = 'питона'
s2 = 'поросенок титан итоги лавка погосут киношы'
# Словарь гласный букв для проверки
glas = {'а': 0, 'у': 0, 'о': 0, 'ы': 0, 'и': 0, 'э': 0, 'я': 0, 'ю': 0, 'ё': 0, 'е': 0, }
# счётчик гласных бук для тестового слова
gls1 = 0
# длина тестовой строки
sl1 = list(s1)
# Цикл посчёта гласных букв в тестовом слове
for i, j in enumerate(sl1):
    # проверка со словарём
    if j in glas:
        # если есть гласная буква, прибавляем переменную
        gls1 += 1
# Выводим на экаран тестовое слово
print('Слова, похожие на:', s1)
# Переводим тестовое слово в список
sl2 = s2.split()
# Узнаём длину тестового слова
lsl2 = len(sl2)
for i in sl2:
    sl3 = list(i)
    lsl3 = len(i)
    gls2 = 0
    #    print(i)
    if lsl2 <= lsl3 + 2 or lsl2 == lsl3:
        for c, v in enumerate(sl1):
            if v in glas:
                if c < lsl3 and sl3[c] in glas:
                    gls2 += 1
        if gls1 == gls2:
            print(''.join(map(str, sl3)))
